# making a right triangle
print("* * * *", end = '\n' '\n' '\n')
print("* * *", end = '\n' '\n' '\n' )
print("* *", end = '\n' '\n' '\n' )
print("*", end = '\n' '\n' '\n' )

# making a isoceles triangle
print("    *", end = '\n' '\n' '\n')
print("   * *", end = '\n' '\n' '\n' )
print("  *   *", end = '\n' '\n' '\n' )
print(" * * * *", end = '\n' '\n' '\n' )

# making a  diamond
print("    *", end = '\n' '\n' '\n')
print("   * *", end = '\n' '\n' '\n' )
print("  *   *", end = '\n' '\n' '\n' )
print(" *     *", end = '\n' '\n' '\n' )
print("  *   *", end = '\n' '\n' '\n' )
print("   * *", end = '\n' '\n' '\n' )
print("    *", end = '\n' '\n' '\n')

# making a solid rectangle
